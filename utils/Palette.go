var palette map[string]sdl.Color

func GetOpaqueColour(name string, opaq uint8) sdl.Color {
	c, found := palette[name]
	if found {
		return sdl.Color{R: c.R, G: c.G, B: c.B, A: opaq}
	}
	return palette["Red"]
}

func GetColour(name string) sdl.Color {
	c, found := palette[name]
	if found {
		return c
	}
	return palette["Red"]
}

